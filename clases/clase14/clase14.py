# Clase 14 - Proyecto Integrador

# ====== OBJETIVO ======
# Desarrollar un programa en Python que simule un sistema de ventas para un E-commerce aplicando los conceptos aprendidos en clase.

# ====== REQUISITOS ======
# Listas:
# - Definir al menos tres listas que contengan productos, precios y stock respectivamente.

# Ticket:
# - El programa debe preguntar qué producto se está vendiendo y en qué cantidad e imprimir esa información y el precio total por consola.

# Función de Validación:
# - Implementar al menos una función que realice una validación relevante para el sistema (por ejemplo, verificar una entrada de teclado que deba ser numérica).

# Estructura cíclica:
# - El programa debe ser cíclico. Después de cada transacción, preguntar al usuario si desea realizar otra transacción o si desea realizar el cierre de caja.

# Cierre de caja:
# - Al salir del programa imprimir en consola la facturación diaria, la cantidad de ventas realizadas y el ticket promedio.

# ====== ASPECTOS A CONSIDERAR ======
# - Utilizar tipos de datos primitivos y listas, condicionales, contadores, acumuladores, ciclos y funciones.
# - El programa debe estar bien estructurado en tres archivos: Un archivo con los datos de los productos, uno con las funciones a utilizar y un archivo main que contenga la lógica principal del sistema.

# ====== ENTREGA ======
# - Entregar el código del programa con los tres archivos a utilizar en un .zip que tenga como nombre de archivo apellido y nombre del alumno.
# - Incluir comentarios que expliquen la lógica de las funciones y decisiones de programación.